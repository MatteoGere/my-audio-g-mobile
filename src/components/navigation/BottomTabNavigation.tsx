export function BottomTabNavigation() {
  const pathname = usePathname();

  return (
    <nav
      className="fixed bottom-0 left-0 right-0 z-40 bg-background/95 backdrop-blur-sm border-t border-surface"
      role="tablist"
    >
      <div className="flex items-center justify-around h-16 px-2">
        {tabs.map((tab) => {
          const isActive = pathname === tab.href || pathname.startsWith(tab.href + '/');
          
          return (
            <Link
              key={tab.href}
              href={tab.href}
              className={cn(
                'flex flex-col items-center justify-center gap-1 px-3 py-2 min-w-0 flex-1 transition-colors duration-200',
                'focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-primary focus-visible:ring-offset-2 rounded-lg',
                isActive ? 'text-primary' : 'text-muted hover:text-foreground'
              )}
              role="tab"
              aria-selected={isActive}
              aria-label={tab.label}
            >
              <div className="w-6 h-6 flex items-center justify-center">
                {isActive && tab.activeIcon ? tab.activeIcon : tab.icon}
              </div>
              <span
                className={cn(
                  'text-xs font-medium truncate',
                  isActive ? 'text-primary' : 'text-muted'
                )}
              >
                {tab.label}
              </span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}